CREATE TABLE BIORXIV_CONTROL_MULTI_CITED AS
SELECT FK_ITEMS, CODE FROM (
SELECT DISTINCT
    t1.FK_ITEMS, t4.CODE, COUNT(t4.CODE) AS CNT,
    MAX(COUNT(t4.CODE)) OVER (PARTITION by t1.FK_ITEMS) as MAXCNT
FROM
    BIORXIV_ALL_CONTROLS t1
INNER JOIN
    SCOPUS_B_2018.CITINGITEMS t2
ON
    t1.FK_ITEMS = t2.FK_ITEMS_CITING
INNER JOIN
    SCOPUS_B_2018.ITEMS_CLASSIFICATIONS t3
ON
    t2.FK_ITEMS_CITED = t3.FK_ITEMS
INNER JOIN
    SCOPUS_B_2018.CLASSIFICATIONS t4
ON
    t4.PK_CLASSIFICATIONS = t3.FK_CLASSIFICATIONS
    AND t4.CLASSIFICATION_TYPE = 'ASJC'
    AND t4.CODE NOT IN ('1000', '1100', '1200', '1300', '1400', '1500', 
                        '1600', '1700', '1800', '1900', '2000', '2100', 
                        '2200', '2300', '2400', '2500', '2600', '2700', 
                        '2800', '2900', '3000', '3100', '3200', '3300', 
                        '3400', '3500', '3600')
GROUP BY t1.FK_ITEMS, t4.CODE)
WHERE
  CNT = MAXCNT