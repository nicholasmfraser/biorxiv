CREATE TABLE BIORXIV_PUBLISHED_SINGLE_CAT AS
SELECT DISTINCT
    ARTICLE_DOI
FROM
    BIORXIV_PUBLISHED_ARTICLES T1
INNER JOIN
    SCOPUS_B_2018.SOURCES_CLASSIFICATIONS T2
ON
    T1.FK_SOURCES = T2.FK_SOURCES
INNER JOIN
    SCOPUS_B_2018.CLASSIFICATIONS t3
ON
    t2.FK_CLASSIFICATIONS = t3.PK_CLASSIFICATIONS
    AND t3.CLASSIFICATION_TYPE = 'ASJC'
    AND t3.CODE NOT IN ('1000', '1100', '1200', '1300', '1400', '1500', 
                        '1600', '1700', '1800', '1900', '2000', '2100', 
                        '2200', '2300', '2400', '2500', '2600', '2700', 
                        '2800', '2900', '3000', '3100', '3200', '3300', 
                        '3400', '3500', '3600')
    